    <h2>Variation in salinity</h2>
    <p>
    The salinity of the oceans corresponds to the amount of salt present in the sea. It is measured in
    PSU (Practical Salinity Unit), which corresponds to the average salinity of the oceans, expressed
    in grams of salt per liter of water. The salinity of the oceans averages 35 PSU, which
    is roughly equivalent to five teaspoons of salt per liter of seawater. According to the Copernicus Marine Service website,
    PSU values vary in certain regions,
    such as the North Atlantic, where salinity is 30 PSU, which is lower than
    the global average. Conversely, it can be as high as 40 PSU in warm seas such as the
    Red Sea.

    Global warming plays a major role in salinity variation. For example,
    when precipitation increases and ice melts, large amounts of fresh water
    flow into the ocean, diluting the salt water and reducing its salinity. Conversely,
    in warm areas where evaporation is very high, salinity is higher,
    as in the Mediterranean or the Persian Gulf. Global warming affects
    each area of the planet differently.
    Changes in salinity have an impact on marine ecosystems where marine species
    such as fish and plankton are sensitive to changes in salinity. For example,
    in estuaries, salt water linked to rising sea levels can disrupt ecosystems. According to
    the UN, in the Tuvalu Islands in the Pacific, freshwater aquifers are
    being submerged by salt water, making drinking water difficult to access and threatening the survival
    of the inhabitants.
    </p>
