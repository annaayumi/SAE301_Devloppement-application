  <h2>Rising temperatures and melting ice</h2>

    <p>
      Global warming refers to an increase in the average temperature around the world. 
      This rise in temperature is caused by several possible phenomena, such as 
      greenhouse gas emissions produced by humans when using various fossil fuels 
      such as coal, oil, or gas, as well as deforestation and certain agricultural practices involving the use of nitrous oxide in nitrogen fertilizers, as well as methane, which causes 28 times more warming than CO2 and is responsible for a 20% increase in temperature, or a 1-degree increase over the course of the century, according to the website inrae.fr. According to the United Nations website, the WMO has estimated that the average global temperature increase was between 1.34 and 1.41 degrees and predicts an increase of up to 1.44 degrees between 2015 and 2034 compared to pre-industrial levels.
      According to the Copernicus Climate Change Service website, rising temperatures are causing major meteorological and environmental changes, with periods of severe drought such as in the summer of 2022, when major rivers such as the Rhine in Germany and the Loire in France experienced extremely low water levels, causing difficulties for river transport and electricity production.
      Global warming has also caused ice to melt, as in the Arctic, where the surface area of sea ice in summer has decreased by 40% since the end of 1970, putting certain animal species such as polar bears, which need ice to hunt seals, on the verge of extinction, according to NASA. Another example is the melting of ice in Greenland, where the country lost 532 billion tons of ice in 2019, causing sea levels to rise by 1.5 millimeters, according to the website WWF.fr.
      Climate change is also causing sea levels to rise. In 2024, the average sea level rose by 10 centimeters due to thermal expansion (= the temperature of seawater increases due to rising air temperatures. Warmer water expands), according to the Science et vie website. Many villages are regularly flooded. For example, the village of Ceton was hit twice by floods in 2024, according to the Ouest-France website, and China experienced numerous floods in regions such as Hunan and Guangxi, where rivers such as the Yangtze and Pearl rivers overflowed. The province of Hunan suffered 100 deaths and significant material damage, according to the Environmental Reports website. 
      Global warming directly affects oceans and seas around the world. They are experiencing an increase in surface temperature, gradual acidification, and a rise in average water levels. These changes have visible effects on marine ecosystems and human populations living near or on coastlines.
      The melting of glaciers and ice caps has a huge impact on sea level rise, and the melting of ice is accelerating every year. ¬≤     In Pacific islands such as Kiribati and the Marshall Islands, certain areas are affected by high tides, forcing residents to relocate their villages, while others are leaving these areas as the situation worsens year after year. 
      The US Environmental Protection Agency explains that sea level rise will cause several problems in the future, such as flooding of coastal wetlands and low-lying areas, accelerated beach erosion, and salinization of groundwater. These consequences threaten our food supply by reducing the amount of available agricultural land and disrupting our ecosystems. For example, in the Mekong Delta in Vietnam, the presence of salt water in rice fields renders them unusable, jeopardizing agricultural production on which millions of people depend.
      The rise in ocean surface temperatures is contributing to the development of extreme weather events such as cyclones and hurricanes. For example, in the United States, Hurricane Katrina in 2005 and Hurricane Maria in 2017 were intensified by the heat of the Atlantic Ocean.
      Ocean currents play an important role in regulating the temperature and climate of our planet. They transport heat, nutrients, and salt from one ocean to another. This current is called thermohaline and depends on two factors: salinity and water temperature. Unfortunately, global warming and melting ice are disrupting this circulation system. When ice melts, a very large amount of fresh water is released into the ocean. This less salty water mixes with the salty water of the oceans, slowing down ocean currents.
 
      The reduction in these currents has a significant impact on marine life. The vertical movements of the oceans allow nutrients to rise to the surface of the water. If circulation decreases, the amount of nutrients available at the surface also decreases, reducing the productivity of the oceans and endangering fish and other marine species. 
      According to the Food and Agriculture Organization of the United Nations, this decline in productivity could threaten populations that rely heavily on fishing for their livelihoods, such as those in developing countries.
      Another problem concerns the oceans' ability to store carbon. The oceans absorb a quarter of the CO2 emitted by human activities. The more the currents decrease, the more this ability to absorb CO2 decreases. This means that more CO2 would remain in the atmosphere and further increase global warming.
      A phenomenon known as the ‚ÄúNorth Atlantic cold spot‚Äù corresponds to a sharp drop in salinity and a cooling of surface waters. Scientists have discovered this phenomenon in Iceland and Greenland.
      According to NASA, this phenomenon is linked to the slowing of the Gulf Stream (the ocean current between the east coast of Florida and North Carolina). If NASA's theory is correct, the consequences could be global, causing disruptions to the rainy seasons in Africa, a rise in sea levels on the east coast of the United States, and upheaval in marine biodiversity.
