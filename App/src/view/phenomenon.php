    <h2>Ocean acidification</h2>

    <p>
    The ocean plays a major role in regulating the climate because it absorbs nearly a quarter of the carbon dioxide (CO‚ÇÇ) produced by human activities. But this process has a negative effect: when it absorbs too much CO‚ÇÇ, the ocean forms carbonic acid, which releases hydrogen ions and increases the acidity of the water. This results in a decrease in pH. Since the beginning of the industrial era, the average pH of the ocean has fallen from around 8.2 to 8.1. This drop of 0.1 units may seem small, but it actually corresponds to an increase in acidity of around 30%. IPCC projections estimate that if emissions continue at the current rate, the pH could fall by a further 0.3 to 0.4 by 2100, making the ocean much more acidic than it has been for millions of years.
    
    This acidification, combined with rising water temperatures, poses a direct threat to calcifying organisms, i.e., those that build their shells or skeletons with calcium carbonate. This includes corals, mollusks such as oysters and mussels, and many crustaceans. Their shells or skeletons become more fragile and dissolve more easily in acidic water. The National Oceanic and Atmospheric Administration (NOAA) reports, for example, that in some areas of the US West Coast, oyster farms have already suffered significant losses because larvae were unable to form their shells properly.
    Coral reefs, which are home to around 25% of the world's marine biodiversity, are particularly vulnerable. According to the UN, warming limited to 1.5¬∞C could lead to the destruction of 70-90% of coral reefs. If the temperature rises by 2¬∞C, almost 100% of reefs would disappear. Australia's Great Barrier Reef, a UNESCO World Heritage Site, has already suffered several massive bleaching events in 2016, 2017, and 2020 due to thermal stress and acidification. (Source: Australian Institute of Marine Science).

    This decline has serious repercussions on biodiversity and human communities.
    Coral reefs play a crucial role as nurseries for many fish, which are a source of food for millions of people.
    Their disappearance therefore means fewer fish and thus a threat to food security, particularly in developing coastal countries. According to the FAO, more than 3 billion people worldwide depend directly on marine resources for food.
    Acidification also affects the base of the marine food chain: phytoplankton and certain species of calcifying plankton such as pteropods, small mollusks that serve as food for many fish and marine mammals. If these organisms disappear, the entire ecosystem will collapse. A study conducted in the Southern Ocean and published in Nature Geoscience showed that in some areas, pteropod shells were already beginning to dissolve due to acidification.
    The economic consequences are also significant. Fishing and aquaculture face direct threats. In the Philippines and Indonesia, millions of people depend on coral reefs for food and tourism-related income. The disappearance of these reefs will not only lead to a loss of biodiversity, but also have an economic impact on these nations.

    As a result, ocean acidification is less visible than sea level rise, but its repercussions are just as severe. It weakens corals, mollusks, crustaceans, and plankton, which form the foundation of marine life. It jeopardizes the food and economic security of millions of people around the world and could profoundly alter the oceans by the end of the century if measures are not taken to reduce CO‚ÇÇ emissions.
  </p>
